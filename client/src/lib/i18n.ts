import { useState, useEffect, useCallback } from "react";

export type Locale = "zh" | "ja" | "en";

let currentLocale: Locale = "zh";
const listeners = new Set<() => void>();

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(l: Locale) {
  if (l !== currentLocale) {
    currentLocale = l;
    listeners.forEach((fn) => fn());
  }
}

export function useLocale(): [Locale, (l: Locale) => void] {
  const [, rerender] = useState(0);
  useEffect(() => {
    const fn = () => rerender((n) => n + 1);
    listeners.add(fn);
    return () => { listeners.delete(fn); };
  }, []);
  return [currentLocale, setLocale];
}

// Translation hook
export function useT() {
  const [locale] = useLocale();
  return useCallback((key: string, params?: Record<string, string | number>): string => {
    const val = (translations[locale] as any)?.[key] || (translations.en as any)?.[key] || key;
    if (!params) return val;
    return Object.entries(params).reduce((s, [k, v]) => s.replace(`{${k}}`, String(v)), val);
  }, [locale]);
}

